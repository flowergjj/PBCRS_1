package com.hkbank.pbcrs.model;

public class PbcrsReportFieldMapping {
	
	private String reportType;
	private String fieldName;
	private String methodName;
	private String serviceName;
	private String mapperName;
	private String mapperMethodName;
	private String reportId;
	private String comments;
	
	public String getReportType() {
		return reportType;
	}
	public void setReportType(String reportType) {
		this.reportType = reportType;
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
	public String getMethodName() {
		return methodName;
	}
	public void setMethodName(String methodName) {
		this.methodName = methodName;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getMapperName() {
		return mapperName;
	}
	public void setMapperName(String mapperName) {
		this.mapperName = mapperName;
	}
	public String getMapperMethodName() {
		return mapperMethodName;
	}
	public void setMapperMethodName(String mapperMethodName) {
		this.mapperMethodName = mapperMethodName;
	}
	public String getReportId() {
		return reportId;
	}
	public void setReportId(String reportId) {
		this.reportId = reportId;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	
	

}
